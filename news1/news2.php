                                HYDERABAD: 
                                HIGHLIGHTS
                                TDP exits NDA over centre's refusal for special status to Andhra Pradesh
                                Last week TDP pulled its two ministers out of the central government
                                The TDP has 16 members in the Lok Sabha<br>
                                 N Chandrababu Naidu's Telugu Desam Party (TDP) has exited Prime Minister Narendra Modi's coalition after weeks of wrangling over special status to his state Andhra Pradesh. The TDP will also move a no-confidence motion against the government in parliament, deciding against playing second fiddle to rival Jaganmohan Reddy's YSR Congress, which submitted notice for such a motion on Thursday.</p>
                                <br>
                                "We will now bring our own No-Confidence Motion which will be better and more perfect than that of YSR Congress," TDP parliamentarian YS Chowdary told NDTV.></P>
                                <br>
                                Indicating that the BJP had moved on, spokesperson GVL Narasimha Rao said: "Far from being a threat, TDP's exit is a timely opportunity for the BJP to grow in Andhra Pradesh."
                                <br>
                                The TDP has 16 members in the Lok Sabha, but its pull-out does not threaten the ruling coalition.
                                <br>
                                Last week the TDP pulled its two ministers out of the central government but had stopped short of walking out of the alliance. 
                                <br>
                                Mr Naidu had said then that he tried to reach out to PM Modi but he "did not come on the line". He also complained that he had visited Delhi 29 times to press for special status, but without luck.
                                <br><br>
                                
                                Despite his resentment, he left a door open for reconciliation. Jagan Reddy's no-trust motion, which threatened to outdo him in Andhra's deeply competitive politics ahead of state elections next year, is seen to have accelerated his exit.
                                <br>
                                The BJP had been resigned to divorce - keeping up efforts to placate its largest ally in the south even as it was seen to flirt with Jagan Reddy's party - but the announcement delivers a huge psychological blow ahead of the 2019 national election and at a time other partners like Shiv Sena have been giving it a rough time.
                                <br>
                                The Sena, which has attacked the BJP unsparingly for some time now and has said it will go it alone in Maharashtra, recently declared that "the Modi wave is over".
                                <br>
                                More allies have grown restive after the BJP's setback in bypolls in Uttar Pradesh and Bihar; the party was stunned as it lost two strongholds in UP on Wednesday.
                                <br>
                                Yesterday, Mr Naidu addressed his party parliamentarians in Delhi through teleconference and reportedly said the bypoll results reflected an anti-Modi and anti-BJP wave.
                                <br>
                                The Chief Minister also told his lawmakers that the BJP was trying to pull a Tamil Nadu in Andhra Pradesh, using both Jagan Reddy and Jana Sena boss Pawan Kalyan against the TDP.
                                <br>
                                Mr Naidu has been furious with the centre for not granting special category status to Andhra Pradesh as promised after Telangana was carved out of it in 2014. Special status would mean a large infusion of central funds to help with the state government's plans to develop its new capital Amravati.
                                <br>
                                But what upset Mr Naidu even more was Pawan Kalyan needling him at a public meeting on Wednesday, directly accusing his son Nara Lokesh, who is a minister in Andhra Pradesh, of corruption.
                                <br>
                                COMMENTSPawan Kalyan also accused the chief minister of having struck a deal with the BJP, agreeing to a special financial package instead of insisting on special category status to Andhra Pradesh.
                                <br>
                                The centre had turned down Mr Naidu's demand for special category status to Andhra Pradesh saying the nomenclature had been done away with, except for hill states and states in the Northeast. But Finance Minister Arun Jaitley had promised the state funds equivalent to what a special category state gets.
